    private void guessButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guessButtonActionPerformed
        String guess = jLabel3.getText().toLowerCase() + jTextField1.getText().toLowerCase();
        if (wordCount == 0) {
            jLabel12.setText("0");
            jLabel13.setText("0");
            jTextField1.setText("");
            JOptionPane.showMessageDialog(rootPane, "You have reached the end of the game!"); 
        }
        else{
            try {
                if (words[index][0].toLowerCase().equals(guess)){
                    JLabel label = new JLabel("Correct!");
                    label.setForeground(Color.blue);
                    JOptionPane.showMessageDialog(rootPane, label);
                    score = score+point;
                    jLabel8.setText(String.valueOf(score));
                    reveals = reveals + 1;
                    jLabel13.setText(String.valueOf(reveals));
                    nextButtonActionPerformed(evt);
                } else {
                    if (trials == 0) {
                        JOptionPane.showMessageDialog(rootPane, "Trial Limit Exceeded! Too bad!");
                        nextButtonActionPerformed(evt);
                    } else {
                        trials = trials - 1;
                        jLabel12.setText(String.valueOf(trials+1));
                        JLabel label = new JLabel("Incorrect! Try again!");
                        label.setForeground(Color.red);
                        JOptionPane.showMessageDialog(rootPane, label);
                        jTextField1.setText("");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Invalid Entry!");
            }
        }
    }//GEN-LAST:event_guessButtonActionPerformed
  
